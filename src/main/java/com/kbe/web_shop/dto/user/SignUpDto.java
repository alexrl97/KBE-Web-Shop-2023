package com.kbe.web_shop.dto.user;

import com.google.gson.Gson;
import com.kbe.web_shop.config.constants.Role;
import com.kbe.web_shop.model.Category;

import javax.persistence.Column;

public class SignUpDto {

    private String firstName;
    private String lastName;

    private String company;
    private String email;
    private String password;

    private Role role;

    private String street;

    private int houseNumber;
    private String city;

    private String zip;

    private String country;


    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public Role getRole() {
        return role;
    }

    public void setRole(Role role) {
        this.role = role;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public int getHouseNumber() {
        return houseNumber;
    }

    public void setHouseNumber(int houseNumber) {
        this.houseNumber = houseNumber;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getZip() {
        return zip;
    }

    public void setZip(String zip) {
        this.zip = zip;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public static SignUpDto fromJsonString(String signUpDtoJsonString) {
        return new Gson().fromJson(signUpDtoJsonString, SignUpDto.class);
    }
}
